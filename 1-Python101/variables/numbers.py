

dec = 10
print(dec)       # Prints in decimal
print(bin(dec))  # Prints in bin
print(hex(dec))  # Prints in hex

binary = 0b11
print(binary)       # Prints in decimal
print(bin(binary))  # Prints in bin
print(hex(binary))  # Prints in hex

hexa = 0xA1
print(hexa)       # Prints in decimal
print(bin(hexa))  # Prints in bin
print(hex(hexa))  # Prints in hex


# Complex numbers use j

x = 1 + 2j
print(x)
