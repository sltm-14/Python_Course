""" SUBSTRACTION """
x = 10 - 3
print(x)      # Output: 7


""" MULTIPLICATION """
x = 10 * 3
print(x)      # Output: 30


""" DIVISION """
x = 10 / 3
print(x)      # Output: 3.333333333333333
x = 10 // 3
print(x)      # Output: 3


""" REMENDER """
x = 10 % 3
print(x)      # Output: 1


""" EXPONENT """
x = 10 ** 3
print(x)      # Output: 1000
