course = "Python Programming"
print(len(course))
print(course[0])  # First letter of the string
print(course[-1])  # Last letter of the string
print(course[-2])  # Second letter from the end
print(course[0:3])  # First 3 letters
print(course[:3])  # Same as before
print(course[0:])  # Entire string

#  String are unmutable
